    class SoundThread extends Thread{
            public void run(){
                while(true){
                    if(Thread.interrupted()) {
                        return;
                    }
                    try{
                        mst.play();
                        return;
                    }catch(InterruptedException e){
                        return;
                    } catch(Exception e) {
                        return;
                    }
                }
            }
    }
    
